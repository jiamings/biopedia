$('.drag').draggable({ 
  appendTo: 'body',
  helper: 'clone'
});

$('#dropzone').droppable({
    activeClass: 'active',
    hoverClass: 'hover',
    accept: ":not(.ui-sortable-helper)", // Reject clones generated by sortable
    drop: function (e, ui) {
        num++;
        //alert(num);
        document.getElementsByName('number')[0].value = num;
        //alert(document.getElementsByName('number').value);
        //alert(document.getElementsByName(number).value);
        var $el = $('<div class="drop-item"><input type="hidden" name="' + num + '" value="' + ui.draggable.text()[0] + '"><details><summary>' + ui.draggable.text() + '</summary></details></div>');
        $el.append($('<button type="button" value="'+num+'"class="btn btn-default btn-xs remove"><span class="glyphicon glyphicon-trash"></span></button>').click(function () {
            if ($(this).val() == num) {
                $(this).parent().detach();
                num--;
                document.getElementsByName('number')[0].value = num;
            }
        }));
        $(this).append($el);
    }
});
